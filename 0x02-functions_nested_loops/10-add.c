/**
 * add - adds two numbers together and returns
 * the result.
 * @i: first number added to the second number
 * @j:second number added to the first number
 *
 * Return: the sum of both numbers
 */
int add(int i, int j)
{
	int k;

	k = i + j;
	return (k);
}

